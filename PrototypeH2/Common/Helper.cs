    // Abstraction for new page data, i.e. usercontrols
    public interface IPageViewModel
    {
        string Name { get; }
    }

    // Abstraction for managing all available usercontrol added to application
    public interface IPageDisplay
    {
        IPageViewModel GetCurrentPage();
        void ChangeViewModel(IPageViewModel newPage);
    }
    #endregion
}
